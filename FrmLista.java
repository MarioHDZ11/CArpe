    private void cargarTexto() {
        try {
            String texto = archivo.leerTodo(rutaArchivo);
            txaLista.setText(texto.isEmpty() ? "Sin vehículos guardados." : texto);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,
               "Error al leer archivo: " + ex.getMessage(),
               "Problema", JOptionPane.ERROR_MESSAGE);
        }
    }

