    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String marca = txtMarca.getText().trim();
        String modelo = txtModelo.getText().trim();

        if (marca.isEmpty() || modelo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa Marca y Modelo.");
            return;
        }

        try {
            boolean ok = archivo.eliminarVehiculo(marca, modelo, rutaArchivo);
            JOptionPane.showMessageDialog(this,
                ok ? "Vehículo eliminado." : "No se encontró ese vehículo.");
            cargarTexto();                       // refresca lista
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,
                "Error al eliminar: " + ex.getMessage(),
                "Problema", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

